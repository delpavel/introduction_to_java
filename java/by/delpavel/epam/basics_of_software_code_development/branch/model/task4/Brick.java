package by.delpavel.epam.basics_of_software_code_development.branch.model.task4;


public class Brick {

    private int x;
    private int y;
    private int z;

    public Brick(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;

    }

    int getX() {
        return x;
    }

    int getY() {
        return y;
    }

    int getZ() {
        return z;
    }
}
